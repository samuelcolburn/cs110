// Program to demonstrate methods
public class MethodDemo
{
   // required main method, execution always begins in main
   public static void main(String [] args)
   {
   
      displayInfo();
      displayMoreInfo();
      displayMoreInfo();
      displayInfo();
   
   
   }
   // method displayInfo displays the string "info"
   public static void displayInfo()
   {
      System.out.println("info");
      
   }
   // method displayMoreInfo displays the string "more info"
   public static void displayMoreInfo()
   {
      System.out.println("more info");
      
   }
}

